package com.Basis.base;

public class Datatype {
    public static void main(String[] args) {
        // 八大基本数据类型

        // 整数
        int num1 = 10;  // 最常用
        byte num2 = 20;     // 最小
        short num3 = 30;
        long num4 = 30L;    // 要加L

        // 浮点数
        float num5 = 40.2F; // 要加F
        double num6 = 3.14159;

        // 字符
        char name = 'A';
        // 字符串(不是关键字，而是一个类)
        String name1 = "monarch";

        // 布尔值：是非
        boolean flag = true;
        boolean flag1 = false;
    }
}
